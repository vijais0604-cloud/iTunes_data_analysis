--ROCK GENRE CUSTOMERS
SELECT
    c.customer_id,
    c.first_name,
    c.last_name,
    c.email,
    COUNT(*) AS rock_purchase_count
FROM customer c
JOIN invoice i ON c.customer_id = i.customer_id
JOIN invoice_line il ON i.invoice_id = il.invoice_id
JOIN track t ON il.track_id = t.track_id
JOIN genre g ON t.genre_id = g.genre_id
WHERE g.name = 'Rock'
GROUP BY c.customer_id, c.first_name, c.last_name
ORDER BY rock_purchase_count DESC;


-- CITY WITH THE MOST CUSTOMERS AND ITS MOST POPULAR GENRE
SELECT
    x.city,
    x.genre_name,
    x.genre_purchase_count
FROM (
    SELECT
        c.city,
        g.name AS genre_name,
        COUNT(il.track_id) AS genre_purchase_count,
        RANK() OVER (
            ORDER BY COUNT(il.track_id) DESC  -- Step 2: rank genres by purchases
        ) AS genre_rnk
    FROM customer c
    JOIN invoice i ON c.customer_id = i.customer_id
    JOIN invoice_line il ON i.invoice_id = il.invoice_id
    JOIN track t ON il.track_id = t.track_id
    JOIN genre g ON t.genre_id = g.genre_id
    WHERE c.city = (
        -- Step 1: find the single city with the most customers
        SELECT city
        FROM customer
        GROUP BY city
        ORDER BY COUNT(DISTINCT customer_id) DESC
        LIMIT 1
    )
    GROUP BY c.city, g.name
) x
WHERE x.genre_rnk = 1;