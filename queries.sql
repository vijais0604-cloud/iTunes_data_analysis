--CUSTOMERS WITH MORE THAN 1 GENRE PURCHASE
SELECT
    c.customer_id,
    c.first_name,
    c.last_name,
    COUNT(DISTINCT g.genre_id) AS genre_count
FROM customer c
JOIN invoice i ON c.customer_id = i.customer_id
JOIN invoice_line il ON i.invoice_id = il.invoice_id
JOIN track t ON il.track_id = t.track_id
JOIN genre g ON t.genre_id = g.genre_id
GROUP BY c.customer_id, c.first_name, c.last_name
HAVING COUNT(DISTINCT g.genre_id) > 1
ORDER BY genre_count DESC;


--MOST USED MEDIA TYPES
SELECT
    mt.name AS media_type,
    COUNT(t.track_id) AS track_count
FROM media_type mt
JOIN track t ON mt.media_type_id = t.media_type_id
GROUP BY mt.media_type_id, mt.name
ORDER BY track_count DESC;


--MOST PURCHASED TRACK COMBINATIONS
SELECT
    t1.name AS track1,
    t2.name AS track2,
    COUNT(*) AS purchase_count
FROM invoice_line il1
JOIN invoice_line il2 ON il1.invoice_id = il2.invoice_id AND il1.invoice_line_id < il2.invoice_line_id
JOIN track t1 ON il1.track_id = t1.track_id
JOIN track t2 ON il2.track_id = t2.track_id
GROUP BY t1.name, t2.name
ORDER BY purchase_count DESC
LIMIT 20;


